public class Exercise7_TypeCasting {
    public static void main(String[] args) {
        // double to int
        double d = 9.99;
        int i = (int) d;
        System.out.println("double to int: " + d + " -> " + i);

        // int to double
        int x = 5;
        double y = (double) x;
        System.out.println("int to double: " + x + " -> " + y);
    }
}